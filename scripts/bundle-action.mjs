// Bundles the GitHub Action entrypoint into a single self-contained file that
// is checked into the repository under action/index.js. GitHub Actions run the
// committed bundle directly, so this must be regenerated whenever action code
// or its dependencies change.
import { build } from 'esbuild';
import { mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');

mkdirSync(resolve(root, 'action'), { recursive: true });

await build({
  entryPoints: [resolve(root, 'src/action/main.ts')],
  outfile: resolve(root, 'action/index.js'),
  bundle: true,
  platform: 'node',
  target: 'node20',
  format: 'cjs',
  minify: false,
  sourcemap: false,
  legalComments: 'none',
  logLevel: 'info',
  banner: {
    js: '// CacheMap GitHub Action bundle — generated by scripts/bundle-action.mjs. Do not edit by hand.',
  },
});

console.log('Bundled GitHub Action to action/index.js');
